#pragma once

#ifndef ROM_BASE_ADDR_UL
#error "ROM_BASE_ADDR_UL must be defined before including mem.h"
#endif

#define ST_RAM_TOP_ADDR_UL 0x00080000
#define ST_STACK_TOP_ADDR_UL (ST_RAM_TOP_ADDR_UL - 32768)
#define ST_RAM_VARS_BASE_ADDR_UL (ST_STACK_TOP_ADDR_UL - (128 * 1024))
#define ST_RAM_VARS_SIZE_BYTES (124 * 1024)
#define ST_RAM_VARS_RESERVED_BYTES 8
#define ST_RAM_VARS_ALLOC_BASE_ADDR_UL \
  (ST_RAM_VARS_BASE_ADDR_UL + ST_RAM_VARS_RESERVED_BYTES)
#define ST_RAM_VARS_ALLOC_SIZE_BYTES \
  (ST_RAM_VARS_SIZE_BYTES - ST_RAM_VARS_RESERVED_BYTES)
#define ST_RAM_EXEC_BASE_ADDR_UL ST_RAM_VARS_ALLOC_BASE_ADDR_UL

#define ST_SCREEN_BASE_ADDR_UL ST_STACK_TOP_ADDR_UL
#define ST_SCREEN_SIZE_BYTES 32000

#if !defined(__ASSEMBLER__)

/* Compile-time layout guards. */
enum {
  ST_LAYOUT_VARS_END = ST_RAM_VARS_BASE_ADDR_UL + ST_RAM_VARS_SIZE_BYTES,
  ST_LAYOUT_SCREEN_END = ST_SCREEN_BASE_ADDR_UL + ST_SCREEN_SIZE_BYTES,
  ST_LAYOUT_CHECK_RESERVED_VARS =
      1 / ((ST_RAM_VARS_RESERVED_BYTES < ST_RAM_VARS_SIZE_BYTES) ? 1 : 0),
  ST_LAYOUT_CHECK_EXEC_IN_VARS =
      1 / (((ST_RAM_EXEC_BASE_ADDR_UL >= ST_RAM_VARS_ALLOC_BASE_ADDR_UL) &&
            (ST_RAM_EXEC_BASE_ADDR_UL < ST_LAYOUT_VARS_END))
               ? 1
               : 0),
  ST_LAYOUT_CHECK_SCREEN_TOP =
      1 / ((ST_LAYOUT_SCREEN_END <= ST_RAM_TOP_ADDR_UL) ? 1 : 0),
  ST_LAYOUT_CHECK_SCREEN_VARS_NON_OVERLAP =
      1 / (((ST_LAYOUT_VARS_END <= ST_SCREEN_BASE_ADDR_UL) ||
            (ST_LAYOUT_SCREEN_END <= ST_RAM_VARS_BASE_ADDR_UL))
               ? 1
               : 0),
  ST_LAYOUT_CHECK_VARS_TOP =
      1 / ((ST_LAYOUT_VARS_END <= ST_RAM_TOP_ADDR_UL) ? 1 : 0)
};

#define ST_SCREEN_BASE_ADDR ((unsigned long)ST_SCREEN_BASE_ADDR_UL)
#define ST_RAM_VARS_BASE_ADDR ((unsigned long)ST_RAM_VARS_BASE_ADDR_UL)

#endif
